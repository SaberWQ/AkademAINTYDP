    document.getElementById('roundResultsScreen').style.display = 'block';
    
    const roundScore = roundScores[currentRound - 1];
    const percentage = (roundScore / 12) * 100;
    const roundData = quizData[Object.keys(quizData)[currentRound - 1]];
    
